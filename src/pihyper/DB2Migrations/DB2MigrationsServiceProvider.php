<?php namespace pihyper\DB2Migrations;

use Illuminate\Support\ServiceProvider;

class DB2MigrationsServiceProvider extends ServiceProvider {

    /**
     * Indicates if loading of the provider is deferred.
     *
     * @var bool
     */
    protected $defer = false;

    /**
     * Boot the service provider.
     *
     * @return void
     */
    public function boot()
    {
    }

    /**
     * Register the service provider.
     *
     * @return void
     */
    public function register()
    {
        $this->app->singleton('artisan.convert.migrations', function($app) {
            return new DB2MigrationsCommand;
        });

        $this->commands('artisan.convert.migrations');
    }

    /**
     * Get the services provided by the provider.
     *
     * @return array
     */
    public function provides()
    {
        return array();
    }

}
